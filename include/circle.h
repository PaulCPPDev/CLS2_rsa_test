#ifndef CIRCLE_H // same as #if !defined(CIRCLE_H)
#define CIRCLE_H

#define PI 3.1415

/**
 * Computes the area of a circle.
 *
 * @param radius The radius of the circle to compute the area for.
 * @return The area of the circle with the given radius.
 */
double area(double radius);

/**
 * Computes the circumference of a circle.
 *
 * @param radius The radius of the circle to compute the circumference for.
 * @return The circumference of the circle with the given radius.
 */
double circumference(double radius);

#endif // CIRCLE_H
